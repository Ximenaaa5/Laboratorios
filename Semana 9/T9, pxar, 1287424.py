
n1 = float(input(" Ingrese Cantidad: "))

millas = n1 / 1609.34
kilometros = n1 / 1000
pies = n1 * 3.28084
pulgadas = pies * 12
yardas = pies / 3

print("Cantidad en millas: "+str(millas)+"millas")
print("Cantidad en kilometros: "+str(kilometros)+"kilometros")
print("Cantidad en pies: "+str(pies)+"pies")
print("Cantidad en pulgadas: "+str(pulgadas)+"pulgadas")
print("Cantidad en yardas: "+str(yardas)+"yardas")